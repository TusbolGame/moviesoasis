     */
    public function index(Request $request)
    {

        $category = $request->category;
        $movies = MoviesUpload::orderBy('id', 'Desc')
            ->where('category', $category)
            ->get();
        return response(['data' => $movies]);
    }

    public function search(Request $request)
    {

        $movie = $request->movie;

        $movies = MoviesUpload::orderBy('id', 'Desc')
            ->where('name', $movie)
            ->get();

        return response(['data' => $movies]);
    }


